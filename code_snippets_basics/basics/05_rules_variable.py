# Variable naming rules in Python

# 1. Variable names must start with a letter (a-z, A-Z) or an underscore (_).
# 2. Variable names can only contain letters, numbers, and underscores.
# 3. Variable names are case-sensitive (e.g., 'myVariable' and 'myvariable' are different).
# 4. Variable names cannot be the same as Python reserved keywords (e.g., 'if', 'for', 'while', etc.).
# Examples of valid variable names
my_variable = 10
_variable2 = "Hello"
myVariable3 = 3.14          
# Examples of invalid variable names (uncomment to see errors)
# 1variable = 5          # Invalid: starts with a number
# my-variable = "Hi"    # Invalid: contains a hyphen
# for = 20              # Invalid: 'for' is a reserved keyword
# Print valid variable names
print(my_variable)  # Output: 10
print(_variable2)   # Output: Hello
print(myVariable3)  # Output: 3.14  


